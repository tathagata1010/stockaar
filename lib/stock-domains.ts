// Strip "Limited"/"Ltd"/"Industries" etc., dedupe spaces, kebab to derive likely company domain.
function deriveDomainFromName(name: string): string | null {
  if (!name) return null;
  const stripped = name
    .replace(/\b(limited|ltd|industries|industry|corporation|corp|company|co|inc|the|of india|india|pvt|private|enterprises|holdings)\b/gi, " ")
    .replace(/[^a-zA-Z0-9 ]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase();
  if (!stripped) return null;
  const slug = stripped.replace(/\s+/g, "");
  if (slug.length < 3 || slug.length > 30) return null;
  return `${slug}.com`;
}

export function getStockDomain(symbol: string, name?: string): string | undefined {
  const direct = STOCK_DOMAINS[symbol.toUpperCase()];
  if (direct) return direct;
  if (name) {
    const guess = deriveDomainFromName(name);
    if (guess) return guess;
  }
  return undefined;
}
